<p class="Titulo2">&iquest;Para qu&eacute; sirve el panel de control? </p>
<p>El panel de control es el modo m&aacute;s gr&aacute;fico que se puede tener para tener un control total del estado de las reservas de las pistas, pudiendo, de un s&oacute;lo vistazo saber no s&oacute;lo si est&aacute;n ocupadas, sino tambi&eacute;n qui&eacute;n las ocupa y si est&aacute;n abonadas o no. La codificaci&oacute;n por colores le hace ser identificado con un solo vistazo. </p>
<p>Este apartado est&aacute; creado para facilitar la gesti&oacute;n de las reservas, pudiendo:</p>
<ul>
  <li class="Bulletpoint">Crear una reserva nueva</li>
  <li class="Bulletpoint">Modificar una reserva ya confirmada</li>
  <li class="Bulletpoint">Anular una reserva</li>
  <li class="Bulletpoint"><span class="Bulletpoint">Marcar como pagada una reserva pendiente de pago</span></li>
  <li class="Bulletpoint">Activar la luz <br />
  </li>
</ul>

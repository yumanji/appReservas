  Tendr&aacute;s que seleccionar el tipo de deporte sobre el cual  realizar tu reserva, luego el tipo de pista (muro, cristal&hellip;etc), el n&uacute;mero de  pista y por supuesto la fecha cu&aacute;ndo quieres jugar.<br />
  Una vez seleccionada la preferencia simplemente hay que  pulsar el bot&oacute;n de &ldquo;Buscar&rdquo;</p>

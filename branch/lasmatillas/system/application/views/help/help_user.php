  En el siguiente paso has de seleccionar la hora deseada, siempre  que est&eacute; disponible. La pista estar&aacute; disponible si figura la casilla en color  verde. Selecciona, por tramos de medias horas, la franja horaria deseada. Una  vez elegida, pulsa el bot&oacute;n &ldquo;confirma tu reserva&rdquo;</p>
